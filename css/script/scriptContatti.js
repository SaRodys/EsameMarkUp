//------------------------INSTAGRAM ICON----------------------
var instagram2 = document.getElementById("instagram2");

function mouseOverIg2(){
    instagram2.src="./css/img/instagramClick.png";
}
function mouseOutIg2() {
    instagram2.src="./css/img/instagram.png";
}

instagram2.onmouseover = function (){
mouseOverIg2();
}
instagram2.onmouseout = function () {
    mouseOutIg2();
}
